pub fn category_label(language: Language, value: &str) -> String {
    CATEGORIES
        .iter()
        .find(|(zh, en)| value == *zh || value == *en)
        .map(|(zh, en)| match language {
            Language::Chinese => (*zh).to_string(),
            Language::English => (*en).to_string(),
        })
        .unwrap_or_else(|| value.to_string())
}

pub fn category_value_from_label(label: &str) -> String {
    CATEGORIES
        .iter()
        .find(|(zh, en)| label == *zh || label == *en)
        .map(|(zh, _)| (*zh).to_string())
        .unwrap_or_else(|| label.to_string())
}
